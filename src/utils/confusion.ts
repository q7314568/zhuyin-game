// 取得容易混淆的注音符號
export const getConfusingSymbols = (target: string, count: number = 2): string[] => {
    const similar = ZHUYIN_CONFUSION_MAP[target] || [];
    // 隨機打亂並選取
    const shuffled = [...similar].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count);
};
